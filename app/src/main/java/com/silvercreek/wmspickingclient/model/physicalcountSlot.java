package com.silvercreek.wmspickingclient.model;

public class physicalcountSlot {
    private String slot;
    private String wmsstat;
    private String posted;
    private String grpcnt;
    private String status;
    private String doclinecount;
    private String rowno;

    public String getslot() {
        return slot;
    }
    public void setslot(String slot) {
        this.slot = slot;
    }
    public String getwmsstat() {
        return wmsstat;
    }
    public void setwmsstat(String wmsstat) {
        this.wmsstat = wmsstat;
    }
    public String getposted() {
        return posted;
    }
    public void setposted(String posted) {
        this.posted = posted;
    }
    public String getgrpcnt() {
        return grpcnt;
    }
    public void setgrpcnt(String grpcnt) {
        this.grpcnt = grpcnt;
    }
    public String getstatus() {
        return status;
    }
    public void setstatus(String status) {
        this.status = status;
    }
    public String getdoclinecount() {
        return doclinecount;
    }
    public void setdoclinecount(String doclinecount) {
        this.doclinecount = doclinecount;
    }
    public String getrowno() {
        return rowno;
    }
    public void setrowno(String rowno) {
        this.rowno = rowno;
    }
}

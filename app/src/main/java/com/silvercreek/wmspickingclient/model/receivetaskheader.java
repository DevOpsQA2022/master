package com.silvercreek.wmspickingclient.model;

public class receivetaskheader {
    private String descrip;
    private String venddescrip;
    private String casecounted;
    private String casetotal;
    private String plttotal;
    private String pltcounted;
    private String wmsdate;

    public String getdescrip() {
        return descrip;
    }
    public void setdescrip(String descrip) {
        this.descrip = descrip;
    }
    public String getvenddescrip() {
        return venddescrip;
    }
    public void setvenddescrip(String venddescrip) {
        this.venddescrip = venddescrip;
    }
    public String getcasecounted() {
        return casecounted;
    }
    public void setcasecounted(String casecounted) {
        this.casecounted = casecounted;
    }
    public String getcasetotal() {
        return casetotal;
    }
    public void setcasetotal(String casetotal) {
        this.casetotal = casetotal;
    }
    public String getplttotal() {
        return plttotal;
    }
    public void setplttotal(String plttotal) {
        this.plttotal = plttotal;
    }
    public String getpltcounted() {
        return pltcounted;
    }
    public void setpltcounted(String pltcounted) {
        this.pltcounted = pltcounted;
    }
    public String getwmsdate() {
        return wmsdate;
    }
    public void setwmsdate(String wmsdate) {
        this.wmsdate = wmsdate;
    }
}

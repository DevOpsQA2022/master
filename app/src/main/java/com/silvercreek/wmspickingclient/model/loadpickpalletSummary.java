package com.silvercreek.wmspickingclient.model;

public class loadpickpalletSummary {
    private String wmsDate;
    private String Truck;
    private String Dock;
    private String Routecnt;
    private String Stopcnt;
    private String Palcnt;
    private String rowNo;
    private String Flag;

    public String getwmsDate() {
        return wmsDate;
    }
    public void setwmsDate(String wmsDate) {
        this.wmsDate = wmsDate;
    }
    public String getTruck() {
        return Truck;
    }
    public void setTruck(String Truck) {
        this.Truck = Truck;
    }
    public String getDock() {
        return Dock;
    }
    public void setDock(String Dock) {
        this.Dock = Dock;
    }
    public String getRoutecnt() {
        return Routecnt;
    }
    public void setRoutecnt(String Routecnt) {
        this.Routecnt = Routecnt;
    }
    public String getStopcnt() {
        return Stopcnt;
    }
    public void setStopcnt(String Stopcnt) {
        this.Stopcnt = Stopcnt;
    }
    public String getPalcnt() {
        return Palcnt;
    }
    public void setPalcnt(String Palcnt) {
        this.Palcnt = Palcnt;
    }
    public String getrowNo() {
        return rowNo;
    }
    public void setrowNo(String rowNo) {
        this.rowNo = rowNo;
    }
    public String getFlag() {
        return Flag;
    }
    public void setFlag(String Flag) {
        this.Flag = Flag;
    }
}
